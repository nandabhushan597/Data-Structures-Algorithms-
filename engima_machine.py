    def rotate(self):
        alphabet = "ABCDEFGHIJKLMNOPQRSTUVWYXYZ"

        #notch values that the first and second rotor must have path to in order for subsequent rotor to begin rotating
        #found notch values on enigma rotor details wikipedia page referenced above 
        y = alphabet.index("Y")
        m = alphabet.index("M")
        
        self.rotor1 = np.roll(self.rotor1, 1, 0)
        if self.rotor1[0][y] == 1: #if there is a path between A and Y begin rotating rotor 2 
            self.rotor2 = np.roll(self.rotor2, 1, 0)
        if self.rotor2[0][m] == 1:  #if there is a path between A and M begin rotating rotor 3 
            self.rotor3 = np.roll(self.rotor3, 1, 0)
                
